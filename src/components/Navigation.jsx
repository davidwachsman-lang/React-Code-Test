function Navigation() {
  const location = useLocation();
  const [isOpen, setIsOpen] = useState(false);
  const { user, signOut } = useAuth();

  const handleLogout = async () => {
    try {
      await signOut();
      setIsOpen(false);
    } catch (error) {
      console.error('Logout error:', error);
    }
  };

  const navItems = [
    { path: '/', label: 'Intake', icon: <IntakeIcon /> },
    { path: '/dispatch', label: 'Dispatch & Scheduling', icon: <DispatchIcon /> },
    { path: '/wip-board', label: 'WIP Board', icon: <WIPBoardIcon /> },
    { path: '/estimating', label: 'Estimating', icon: <EstimatingIcon /> },
    { path: '/job-files', label: 'Job Files', icon: <JobFilesIcon /> },
    { path: '/job-file-checks', label: 'Job File Checks', icon: <JobFileChecksIcon /> },
    { path: '/crm', label: 'CRM', icon: <CRMIcon /> },
    { path: '/field-services', label: 'Field Services', icon: <FieldServicesIcon /> },
    { path: '/reporting', label: 'Reporting & Analytics', icon: <ReportingIcon /> },
    { path: '/storm', label: 'Storm', icon: <StormIcon /> },
    { path: '/resources', label: 'Resource Center', icon: <ResourceCenterIcon /> },
    { path: '/goals', label: 'Goals', icon: <GoalsIcon /> },
    { path: '/expectations-2026', label: '2026 Expectations', icon: <Expectations2026Icon /> },
    { path: '/sandbox', label: 'Sandbox', icon: <SandboxIcon /> }
  ];

  const isActive = (path) => {
    return location.pathname === path;
  };

  const handleNavClick = () => {
    setIsOpen(false);
  };

  return (
    <>
      {/* Hamburger button - always visible when nav is closed */}
      <button 
        className={`nav-hamburger ${isOpen ? 'hidden' : ''}`}
        onClick={() => setIsOpen(true)}
        aria-label="Open navigation"
      >
        <HamburgerIcon />
      </button>

      {/* Overlay backdrop */}
      {isOpen && (
        <div 
          className="nav-overlay" 
          onClick={() => setIsOpen(false)}
        />
      )}

      {/* Navigation drawer */}
      <nav className={`navigation ${isOpen ? 'open' : ''}`}>
        <div className="nav-header">
          <h1 className="nav-title">BETA TEST</h1>
          <button 
            className="nav-close"
            onClick={() => setIsOpen(false)}
            aria-label="Close navigation"
          >
            <CloseIcon />
          </button>
        </div>
        <ul className="nav-list">
          {navItems.map((item) => (
            <li key={item.path}>
              <Link 
                to={item.path} 
                className={`nav-link ${isActive(item.path) ? 'active' : ''}`}
                onClick={handleNavClick}
              >
                <span className="nav-icon">{item.icon}</span>
                <span className="nav-label">{item.label}</span>
              </Link>
            </li>
          ))}
        </ul>
        {user && (
          <div className="nav-footer">
            <div className="nav-user-info">
              <span className="nav-user-email">{user.email}</span>
            </div>
            <button className="nav-logout-btn" onClick={handleLogout}>
              <LogoutIcon />
              <span>Sign Out</span>
            </button>
          </div>
        )}
      </nav>
    </>
  );
}

export default Navigation;
